public class Tile {
    private int tile1;
    private int tile2;
    private int tile3;

    public Tile() {
        this.tile1 = 2;
        this.tile2 = 2;
        this.tile3 = 9; //9 is volcano
    }

    public int getTile1() {
        return this.tile1;
    }

    public int getTile2() {
        return this.tile2;
    }

    public int getTile3() {
        return this.tile3;
    }
}
